from math import pi
print("Menghitung Luas dan Volume Bola")

# varibel
jari_jari = 30

# nilai pi

# Rumus luas permukaan bola
luas_permukaan = 4 * pi * (jari_jari ** 2)

# Rumus volume bola
volume = (4/3) * pi * (jari_jari ** 3)

# output
print("Luas Permukaan Bola: ", luas_permukaan)
print("Volume Bola: ", volume)
